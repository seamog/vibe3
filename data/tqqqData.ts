const parseData = (): StockData[] => {
  return rawCSVData
    .trim()
    .split('\n')
    .slice(1) // Skip header
    .map(line => {
      // "2025- 11- 11","111.78",... -> remove quotes and split
      const columns = line.replace(/"/g, '').split(',');
      if (columns.length < 6) return null;
      
      const [dateStr, close, open, high, low, volume] = columns;
      
      return {
        date: new Date(dateStr.replace(/ /g, '')),
        close: parseFloat(close),
        open: parseFloat(open),
        high: parseFloat(high),
        low: parseFloat(low),
        volume: parseVolume(volume),
      };
    })
    .filter((d): d is StockData => d !== null)
    .reverse(); // Data is given in reverse chronological order
};

